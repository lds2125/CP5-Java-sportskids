-- DDL Oracle para a tabela de Brinquedos
CREATE TABLE TDS_MVC_TB_BRINQUEDOS (
  ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  NM_BRINQUEDO VARCHAR2(100) NOT NULL,
  DS_CATEGORIA VARCHAR2(50) NOT NULL,
  NR_FAIXA_MIN NUMBER(2) NOT NULL,
  NR_FAIXA_MAX NUMBER(2) NOT NULL,
  VL_PRECO NUMBER(12,2) NOT NULL,
  QT_ESTOQUE NUMBER(10) NOT NULL,
  NM_MARCA VARCHAR2(50),
  ST_ATIVO NUMBER(1) DEFAULT 1 NOT NULL,
  DS_DESCRICAO VARCHAR2(1000)
);
